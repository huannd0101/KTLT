#include<bits\stdc++.h>
using namespace std;
int main(){
    int a, b;
    cout << "Nhap a = ";
    cin >> a;
    cout << "Nhap b = ";
    cin >> b;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << a << " / " << b << " = " << 1.0 * a / b << endl;
    cout << a << " % " << b << " = " << a % b << endl;
    return 0;
}